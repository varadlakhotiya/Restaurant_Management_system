CREATE TABLE products (
    id INT AUTO_INCREMENT PRIMARY KEY, -- Unique ID for each product
    category VARCHAR(50) NOT NULL,     -- Category (e.g., Spices, Beverages, Snacks)
    name VARCHAR(100) NOT NULL,        -- Product name
    description TEXT,                  -- Product description
    price DECIMAL(10, 2) NOT NULL,     -- Product price
    image_url VARCHAR(255),            -- URL of the product image
    rating DECIMAL(3, 2),              -- Product rating (e.g., 4.5)
    review_count INT,                  -- Number of reviews
    badge VARCHAR(50),                 -- Badge (e.g., Best Seller, New Arrival)
    stock_availability VARCHAR(50),    -- Stock status (e.g., In Stock, Only 3 Left)
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP -- Timestamp of when the product was added
);



